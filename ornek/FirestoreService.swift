    func updateTransaction(walletId: String, transaction: Transaction) async throws {
        guard let transactionId = transaction.id else { return }
        let walletRef = db.collection("wallets").document(walletId)
        let transactionRef = walletRef.collection("transactions").document(transactionId)
        
        // 2. Update Transaction
        try transactionRef.setData(from: transaction, merge: true)
        
        // 3. Sync with Linked Debt if applicable
        if let debtId = transaction.linkedDebtId {
             // Fetch Debt
             let debtRef = walletRef.collection("debts").document(debtId)
             var debt = try await debtRef.getDocument(as: Debt.self)
             
             // Check if amount changed
             if transaction.amount != debt.installmentAmount {
                 // User corrected the installment amount.
                 // We should update the Debt definition to match this correction.
                 // Assumption: The user wants to fix the ENTIRE debt plan based on this correction.
                 
                 debt.installmentAmount = transaction.amount
                 debt.totalAmount = Double(debt.totalInstallments) * debt.installmentAmount
                 
                 // Recalculate remaining amount:
                 // Remaining = (Total - PaidCount) * NewInstallment
                 // Note: We assume 'paidInstallments' logic is handled elsewhere (e.g., creating this transaction incremented it).
                 // So remaining is simply the rest.
                 let remainingCount = debt.totalInstallments - debt.paidInstallments
                 debt.remainingAmount = Double(remainingCount) * debt.installmentAmount
                 
                 // Save Debt Update
                 try debtRef.setData(from: debt, merge: true)
             }
        }
    }
    
    // Legacy fetch (kept for reference or specific use cases)
    func fetchTransactions(walletId: String, limit: Int = 20, lastDoc: DocumentSnapshot? = nil, type: TransactionType? = nil) async throws -> (transactions: [Transaction], lastDoc: DocumentSnapshot?) {
        let walletRef = db.collection("wallets").document(walletId)
        var query: Query = walletRef.collection("transactions")
        
        // 1. Filter first (optimization suggestion)
        if let type = type {
            query = query.whereField("type", isEqualTo: type.rawValue)
        }
        
        // 2. Then Sort & Limit
        query = query.order(by: "date", descending: true)
            .limit(to: limit)

        
        if let lastDoc = lastDoc {
            query = query.start(afterDocument: lastDoc)
        }
        
        let snapshot = try await query.getDocuments()
        let transactions = snapshot.documents.compactMap { try? $0.data(as: Transaction.self) }
        return (transactions, snapshot.documents.last)
    }
    
    // Simple statistics fetch (Client-side calculation for now as aggregation queries might be complex/costly without specific design)
    // For a real production app with massive data, use Firestore Aggregation Queries or Cloud Functions.
    func fetchTransactionStats(walletId: String, from date: Date) async throws -> (income: Double, expense: Double) {
        let walletRef = db.collection("wallets").document(walletId)
        let snapshot = try await walletRef.collection("transactions")
            .whereField("date", isGreaterThanOrEqualTo: date)
            .getDocuments()
        
        var income: Double = 0
        var expense: Double = 0
        
        for doc in snapshot.documents {
            guard let transaction = try? doc.data(as: Transaction.self) else { continue }
            if transaction.type == .income {
                income += transaction.amount
            } else {
                expense += transaction.amount
            }
        }
        
        return (income, expense)
    }
    
    // Calcluate Savings Balance by fetching all relevant transactions and filtering client-side
    // This aligns with the optimistic logic and supports "Contains" matching.
    // Enhanced signature to return debug info if needed (tuple)
    func fetchSavingsBalance(walletId: String) async throws -> (balance: Double, debugInfo: String) {
        let walletRef = db.collection("wallets").document(walletId)
        
        // Fetch all transactions (optimized by ordering if needed, but for MVP just get all)
        let snapshot = try await walletRef.collection("transactions").getDocuments()
        
        var balance: Double = 0
        var processedCount = 0
        var matchCount = 0
        var errorCount = 0
        
        var expCount = 0
        var incCount = 0
        var rawSum: Double = 0
        
        for doc in snapshot.documents {
            do {
                let transaction = try doc.data(as: Transaction.self)
                processedCount += 1
                
                // Logic:
                // + Expense to "Birikim" or "Yatırım" (Money into Savings)
                // - Income ONLY from "Birikim Bozdurma" (Money withdrawn from Savings)
                // We must NOT subtract generic Investment Income (Dividends/Profit) as that creates a massive deficit.
                
                if transaction.type == .expense {
                    // Check Main Category for "Birikim" or "Yatırım"
                    if transaction.categoryName.localizedCaseInsensitiveContains("Birikim") || 
                       transaction.categoryName.localizedCaseInsensitiveContains("Yatırım") {
                        matchCount += 1
                        balance += transaction.amount
                        rawSum += transaction.amount
                        expCount += 1
                    }
                } else if transaction.type == .income {
                    // Check Sub Category for "Birikim" or "Bozdurma" specifically
                    // Or if user named a main category "Birikim Geliri" etc.
                    if transaction.subCategoryName.localizedCaseInsensitiveContains("Birikim") || 
                       transaction.subCategoryName.localizedCaseInsensitiveContains("Bozdurma") {
                        matchCount += 1
                        balance -= transaction.amount
                        rawSum -= transaction.amount
                        incCount += 1
                    }
                }
            } catch {
                errorCount += 1
                // print("Decoding error for doc \(doc.documentID): \(error)")
            }
        }
        
        return (max(0, balance), "M:\(matchCount) | E:\(expCount) I:\(incCount) | Sum:\(Int(rawSum)) | Err:\(errorCount)")
    }
    
