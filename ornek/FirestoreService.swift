    /// Create a new wallet
    func createWallet(name: String, type: WalletType, context: WalletContext, performAsUser uid: String) async throws -> Wallet {
        // 1. Check Pro Status and Wallet Limit
        let userDoc = try await db.collection("users").document(uid).getDocument()
        if let user = try? userDoc.data(as: User.self), !user.isPro {
            // Count existing wallets where user is owner
            let snapshot = try await db.collection("wallets")
                .whereField("ownerId", isEqualTo: uid)
                .getDocuments()
            
            if snapshot.documents.count >= 2 {
                throw NSError(domain: "Finvo", code: 403, userInfo: [NSLocalizedDescriptionKey: "Ücretsiz planda en fazla 2 cüzdan oluşturabilirsiniz. Sınırsız cüzdan için Pro'ya yükseltin."])
            }
        }

        let newWalletRef = db.collection("wallets").document()
        
        let wallet = Wallet(
            id: newWalletRef.documentID,
            name: name,
            ownerId: uid,
            type: type,
            context: context,
            members: [uid],
            permissions: [uid: "owner"]
        )
        
        try newWalletRef.setData(from: wallet)
        
        // Optimistic Update
        DispatchQueue.main.async {
            self.wallets.append(wallet)
        }
        
        return wallet
    }
    
    func createWallet(name: String, type: WalletType, context: WalletContext) async throws -> Wallet {
        guard let uid = AuthenticationManager.shared.user?.uid else {
            throw NSError(domain: "Auth", code: 401, userInfo: [NSLocalizedDescriptionKey: "User not logged in"])
        }
        
        let newWallet = Wallet(
            name: name,
            ownerId: uid,
            type: type,
            context: context,
            members: [uid],
            permissions: [uid: "owner"],
            monthlyLimit: 0,
            savingsGoal: 0
        )
        
        // Add to Firestore
        try db.collection("wallets").document(newWallet.id ?? UUID().uuidString).setData(from: newWallet)
        
        // Optimistic Update can be tricky if ID is auto-generated by Firestore? 
        // Here we use UUID().uuidString if nil, but Wallet struct usually has UUID.
        // Actually Wallet struct has `id`? Let's assume it keeps the ID.
        // If we use `addDocument`, Firestore generates ID.
        // Let's use `setData` with the ID from struct.
        
        DispatchQueue.main.async {
            self.wallets.append(newWallet)
        }
        
        return newWallet
    }
    
    /// Listen for wallets where the user is a member
    func startListeningWallets(forUser uid: String) {
        // If already listening for this user, do nothing to prevent flicker
        if walletsListener != nil && currentListenerUserId == uid {
            return
        }
        
        // Remove existing listener if any (for different user)
        stopListeningWallets()
        
        currentListenerUserId = uid
        
        // Query: wallets where 'members' array contains uid
        // Note: 'array-contains' requires an index potentially.
        walletsListener = db.collection("wallets")
            .whereField("members", arrayContains: uid)
            .addSnapshotListener { [weak self] querySnapshot, error in
                guard let self = self else { return }
                
                if let error = error {
                    print("Error listening for wallets: \(error)")
                    return
                }
                
                guard let documents = querySnapshot?.documents else {
                    self.wallets = []
                    return
                }
                
                self.wallets = documents.compactMap { document in
                    try? document.data(as: Wallet.self)
                }
            }
    }
    
    func getWallet(id: String) async throws -> Wallet {
        let doc = try await db.collection("wallets").document(id).getDocument()
        return try doc.data(as: Wallet.self)
    }
    
    func stopListeningWallets() {
        walletsListener?.remove()
        walletsListener = nil
        wallets = []
    }
    
